/// Whisper medium/large 模型有时会输出繁体字（尤其是台湾口音或训练数据偏向）。
/// 此模块在后处理阶段将繁体字统一转为简体，保证所有输出文件一致。

use std::collections::HashMap;
use std::sync::OnceLock;

/// 将输入文本中的繁体字全部替换为简体字
pub fn simplify(text: &str) -> String {
    if !contains_traditional(text) {
        return text.to_string();
    }
    let map = t2s_map();
    text.chars().map(|c| map.get(&c).copied().unwrap_or(c)).collect()
}

/// 快速检测文本中是否包含繁体字（避免无意义的 HashMap 查找）
